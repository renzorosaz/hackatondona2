package pe.com.tintegro.sigs.entidad;

public class Organizacion {
	
	private Integer idOrganizacion;
	private String nombre;
	private String direccion;
	private String nombreResponsable;
	private String apellidoResponsable;
	private Integer telefonoResponsable;
	private String fotoResponsable;
	private String dniResponsable;
	private Boolean habilitado;
	private String usuario;
	private String clave;
	
	public Integer getIdOrganizacion() {
		return idOrganizacion;
	}
	public void setIdOrganizacion(Integer idOrganizacion) {
		this.idOrganizacion = idOrganizacion;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getDireccion() {
		return direccion;
	}
	public void setDireccion(String direccion) {
		this.direccion = direccion;
	}
	public String getNombreResponsable() {
		return nombreResponsable;
	}
	public void setNombreResponsable(String nombreResponsable) {
		this.nombreResponsable = nombreResponsable;
	}
	public String getApellidoResponsable() {
		return apellidoResponsable;
	}
	public void setApellidoResponsable(String apellidoResponsable) {
		this.apellidoResponsable = apellidoResponsable;
	}
	public Integer getTelefonoResponsable() {
		return telefonoResponsable;
	}
	public void setTelefonoResponsable(Integer telefonoResponsable) {
		this.telefonoResponsable = telefonoResponsable;
	}
	public String getFotoResponsable() {
		return fotoResponsable;
	}
	public void setFotoResponsable(String fotoResponsable) {
		this.fotoResponsable = fotoResponsable;
	}
	public String getDniResponsable() {
		return dniResponsable;
	}
	public void setDniResponsable(String dniResponsable) {
		this.dniResponsable = dniResponsable;
	}
	public Boolean getHabilitado() {
		return habilitado;
	}
	public void setHabilitado(Boolean habilitado) {
		this.habilitado = habilitado;
	}
	public String getUsuario() {
		return usuario;
	}
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	public String getClave() {
		return clave;
	}
	public void setClave(String clave) {
		this.clave = clave;
	}
	
	
	

}
