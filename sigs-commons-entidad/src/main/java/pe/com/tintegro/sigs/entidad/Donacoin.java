package pe.com.tintegro.sigs.entidad;

public class Donacoin {

	private Integer idDonacoin;
	private Integer puntosGanados;
	private Integer puntosVerificados;
	private Integer puntosCanjeados;
	private Integer idUsuario;
	private Integer numeroDonaciones;
	
	
	
	public Integer getNumeroDonaciones() {
		return numeroDonaciones;
	}
	public void setNumeroDonaciones(Integer numeroDonaciones) {
		this.numeroDonaciones = numeroDonaciones;
	}
	public Integer getIdDonacoin() {
		return idDonacoin;
	}
	public void setIdDonacoin(Integer idDonacoin) {
		this.idDonacoin = idDonacoin;
	}
	public Integer getPuntosGanados() {
		return puntosGanados;
	}
	public void setPuntosGanados(Integer puntosGanados) {
		this.puntosGanados = puntosGanados;
	}
	public Integer getPuntosVerificados() {
		return puntosVerificados;
	}
	public void setPuntosVerificados(Integer puntosVerificados) {
		this.puntosVerificados = puntosVerificados;
	}
	public Integer getPuntosCanjeados() {
		return puntosCanjeados;
	}
	public void setPuntosCanjeados(Integer puntosCanjeados) {
		this.puntosCanjeados = puntosCanjeados;
	}
	public Integer getIdUsuario() {
		return idUsuario;
	}
	public void setIdUsuario(Integer idUsuario) {
		this.idUsuario = idUsuario;
	}
	
	
}
